name = "akash"

# Concatenation
greeting = "Hello, " + name 
print(greeting)


# Repetition : * joins and repeats the string n number of times
print(name*3)
print(greeting*5)


# Length of Strings
print(len(name))
print(len(greeting))


#Indexing and slicing

#indexing
print(name[0])  #a
print(name[1])  #k
print(name[2])  #a
print(name[3])  #s
print(name[4])  #h

#slicing   : [a:b] resulting string starts at "a" ends at "b-1"
print(name[0:2])  #ak
print(name[0:3])  #aka
print(name[0:4])  #akas

str1 = "universityofwindsor"
