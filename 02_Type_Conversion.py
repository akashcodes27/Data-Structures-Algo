#Type Conversion - built-in functions 
#To change datatype of a variable using built-in functions 

#Functions:
# str(), float(), int(), bool() 


x1 = 5 
x2 = str(x1)
print(type(x1))
print(type(x2))

y1 = 14
y2 = float(y1)
print(y1)
print(y2)

z1 = "38"
z2 = int(z1)
print(type(z1))
print(type(z2))

m1 = 1
m2 = bool(m1)
print(m2)

m3 = 0 
m4 = bool(m3)
print(m4)


